package Functions.Practise_question;

public class Check_Even_Or_Odd {
    
    public static void ceo(int n){
        if(n % 2 == 0){
            System.out.println("True");
        }
        else{
            System.out.println("False");
        }
    }

    public static void main(String[] args) {
        ceo(3);
    }
    
}
